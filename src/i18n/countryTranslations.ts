/**
 * Returns the localized form of an English country name. Falls back to the
 * English string when no translation exists for the active locale.
 */
export function localizeCountryName(englishName: string, locale: Locale): string {
  if (locale === 'en') return englishName;
  return countryNameTranslations[englishName]?.[locale] ?? englishName;
}

